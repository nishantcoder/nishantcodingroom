/*increment decreament operators file name incd1.cpp*/
#include <iostream>
using namespace std;
int main()
{
    int a=1;
    cout<<"a's value is now "<<a++<<"\n";
    cout<<"a's value is now "<<a<<"\n";
    a=1;
    cout<<"a's value is now "<<++a<<"\n";
    cout<<"a's value is now "<<a<<"\n";
    a=1;
    cout<<"a's value is now "<<a--<<"\n";
    cout<<"a's value is now "<<a<<"\n";
    a=1;
    cout<<"a's value is now "<<--a<<"\n";
    cout<<"a's value is now "<<a<<"\n";
    return 0;
}